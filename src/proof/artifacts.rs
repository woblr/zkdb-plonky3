// ─────────────────────────────────────────────────────────────────────────────
// ProofSystemKind — explicit label on what cryptographic guarantees exist
// ─────────────────────────────────────────────────────────────────────────────

/// Describes the actual proof system used to generate an artifact.
///
/// This enum is the single source of truth for what cryptographic properties
/// a `ProofArtifact` actually carries. It is included in every artifact and
/// every `VerificationResult` so downstream consumers can reason about
/// security properties without inspecting backend internals.
///
/// ### Ordering from weakest to strongest guarantees
///
/// `None` < `HashChainAudit` < `Plonky2Snark` ≈ `Halo2Snark` < (future recursive)
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum ProofSystemKind {
    /// No proof system. Used as a sentinel in invalid/error results.
    /// - is_zero_knowledge: false
    /// - is_succinct: false
    /// - has_real_constraints: false
    None,

    /// Hash-chain audit log produced by ConstraintCheckedBackend.
    ///
    /// - is_zero_knowledge: false  (verifier sees witness digests)
    /// - is_succinct: false        (verification is O(columns × rows))
    /// - has_real_constraints: true (operator invariants are enforced)
    /// - has_polynomial_commitments: false
    /// - has_elliptic_curves: false
    HashChainAudit,

    /// Plonky2 SNARK (FRI-based, Goldilocks field).
    ///
    /// - is_zero_knowledge: true (with blinding)
    /// - is_succinct: true (O(log n) verification)
    /// - has_real_constraints: true
    /// - has_polynomial_commitments: true (FRI)
    /// - has_elliptic_curves: false
    Plonky2Snark,

    /// Plonky3 STARK (FRI-based, BabyBear field, p3-uni-stark).
    ///
    /// - is_zero_knowledge: true (FRI blinding)
    /// - is_succinct: true (O(log n) verification)
    /// - has_real_constraints: true (selector boolean constraint active; more in progress)
    /// - has_polynomial_commitments: true (FRI + MerkleTreeMmcs)
    /// - has_elliptic_curves: false
    Plonky3Stark,

    /// Halo2 SNARK (IPA / KZG based).
    ///
    /// - is_zero_knowledge: true
    /// - is_succinct: true
    /// - has_real_constraints: true
    /// - has_polynomial_commitments: true (KZG or IPA)
    /// - has_elliptic_curves: true
    Halo2Snark,
}

impl ProofSystemKind {
    /// Whether proofs generated by this system carry a zero-knowledge property.
    pub fn is_zero_knowledge(&self) -> bool {
        matches!(self, Self::Plonky2Snark | Self::Plonky3Stark | Self::Halo2Snark)
    }

    /// Whether verification is succinct (sub-linear in witness size).
    pub fn is_succinct(&self) -> bool {
        matches!(self, Self::Plonky2Snark | Self::Plonky3Stark | Self::Halo2Snark)
    }

    /// Whether real operator constraints (sort order, group boundaries, etc.)
    /// are enforced during proving.
    pub fn has_real_constraints(&self) -> bool {
        matches!(
            self,
            Self::HashChainAudit | Self::Plonky2Snark | Self::Plonky3Stark | Self::Halo2Snark
        )
    }

    /// Whether a polynomial commitment scheme is used.
    pub fn has_polynomial_commitments(&self) -> bool {
        matches!(self, Self::Plonky2Snark | Self::Plonky3Stark | Self::Halo2Snark)
    }

    /// Human-readable summary for display in benchmark output and API responses.
    pub fn display_summary(&self) -> &'static str {
        match self {
            Self::None => "No proof system (mock/stub only)",
            Self::HashChainAudit => {
                "Hash-chain audit (real constraints, NOT zero-knowledge, NOT succinct)"
            }
            Self::Plonky2Snark => "Plonky2 SNARK (FRI-based, Goldilocks, zero-knowledge, succinct)",
            Self::Plonky3Stark => {
                "Plonky3 STARK (FRI-based, BabyBear, zero-knowledge, succinct; selector constraint active)"
            }
            Self::Halo2Snark => "Halo2 SNARK (IPA/KZG, zero-knowledge, succinct)",
        }
    }
}

