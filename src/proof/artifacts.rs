// ─────────────────────────────────────────────────────────────────────────────
// Proof store (in-memory)
// ─────────────────────────────────────────────────────────────────────────────

use crate::types::{ZkDbError, ZkResult};
use dashmap::DashMap;
use std::sync::Arc;

pub struct InMemoryProofStore {
    proofs: Arc<DashMap<ProofId, ProofArtifact>>,
}

impl InMemoryProofStore {
    pub fn new() -> Self {
        Self {
            proofs: Arc::new(DashMap::new()),
        }
    }

    pub fn save(&self, artifact: ProofArtifact) {
        self.proofs.insert(artifact.proof_id.clone(), artifact);
    }

    pub fn get(&self, id: &ProofId) -> ZkResult<ProofArtifact> {
        self.proofs
            .get(id)
            .map(|a| a.clone())
            .ok_or_else(|| ZkDbError::internal(format!("proof not found: {}", id)))
    }

    pub fn list_for_query(&self, query_id: &QueryId) -> Vec<ProofArtifact> {
        self.proofs
            .iter()
            .filter(|a| &a.query_id == query_id)
            .map(|a| a.clone())
            .collect()
    }
}

impl Default for InMemoryProofStore {
    fn default() -> Self {
        Self::new()
    }
}
